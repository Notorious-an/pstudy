'''
Casting
    변수에 유형을 지정하려는 경우 캐스팅으로 가능
    특정변수에 유형을 지정하는 것을 casting이라고 함
'''

# 정수형
x = int(1)
print(x)
y = int(2.4)  # 실수를 정수로 casting 했음
print(y)
z = str(3)
print(type(z))
a = int("3")
print(type(a))

# 실수형
x = float(1)

# 문자열
x = str(1)
y = str(2)
print(x)
print(x+y) #답은 12

#아스키코드 변환
a = ord('A')
print(a) # 아스키코드 65
b = chr(65)
print(b)  # 65를 A로 변환

